package net.forthecrown.grenadier;

/**
 * Vanilla permission levels
 */
public enum PermissionLevel {
  /**
   * Level held by all players
   */
  ALL,

  /**
   * Moderators
   */
  MODERATORS,

  /**
   * Game masters
   */
  GAME_MASTERS,

  /**
   * Admins
   */
  ADMINS,

  /**
   * Server owners
   */
  OWNERS;
}